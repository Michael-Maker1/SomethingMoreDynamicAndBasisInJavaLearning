package accountbalance;

public class SplashFigure {

	public static void main(String[] args) {	
		for(int line=1; line<=6; line++) {
			for(int i=1; i<=2*(line-1); i++) {
				System.out.print("\\");
			}
			for(int i=1;i<=(-4*line)+26;i++) {
				System.out.print("!");
			}
			for(int i=1; i<=2*(line-1); i++) {
				System.out.print("/");
		}
			System.out.println();
		 
		
}
		}
	}